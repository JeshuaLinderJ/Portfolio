'use client';

import { motion } from 'framer-motion';
import { ReactNode } from 'react';

interface AnimatedIconProps {
  children: ReactNode;
  className?: string;
  delay?: number;
  hoverScale?: number;
  hoverRotate?: number;
  color?: 'blue' | 'green' | 'purple' | 'orange' | 'red' | 'teal' | 'gray';
  size?: 'sm' | 'md' | 'lg' | 'xl';
}

const colorClasses = {
  blue: 'text-accent-blue bg-blue-50 border-blue-200',
  green: 'text-accent-green bg-green-50 border-green-200',
  purple: 'text-accent-purple bg-purple-50 border-purple-200',
  orange: 'text-accent-orange bg-orange-50 border-orange-200',
  red: 'text-accent-red bg-red-50 border-red-200',
  teal: 'text-accent-teal bg-teal-50 border-teal-200',
  gray: 'text-primary-600 bg-surface-100 border-surface-200',
};

const sizeClasses = {
  sm: 'w-8 h-8 p-1.5',
  md: 'w-10 h-10 p-2',
  lg: 'w-12 h-12 p-2.5',
  xl: 'w-16 h-16 p-3',
};

export default function AnimatedIcon({
  children,
  className = '',
  delay = 0,
  hoverScale = 1.1,
  hoverRotate = 5,
  color = 'blue',
  size = 'md',
}: AnimatedIconProps) {
  return (
    <motion.div
      initial={{ opacity: 0, scale: 0.8 }}
      animate={{ opacity: 1, scale: 1 }}
      transition={{ 
        delay,
        duration: 0.4,
        ease: 'easeOut'
      }}
      whileHover={{ 
        scale: hoverScale,
        rotate: hoverRotate,
        transition: { duration: 0.2 }
      }}
      whileTap={{ scale: 0.95 }}
      className={`
        ${sizeClasses[size]}
        ${colorClasses[color]}
        rounded-lg border flex items-center justify-center
        cursor-pointer transition-all duration-200
        hover:shadow-soft
        ${className}
      `}
    >
      {children}
    </motion.div>
  );
}

